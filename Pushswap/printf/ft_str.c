#include "ft_printf.h"

void	ft_str(char *str, size_t *len)
{
	size_t	x;

	x = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		*len += 6;
		return ;
	}
	while (str[x] != '\0')
	{
		write(1, &str[x], 1);
		(*len)++;
		x++;
	}
}
