#include "libft.h"

int	ft_isalnum(int x)
{
	if ((x >= 48 && x <= 57)
		|| (x >= 65 && x <= 90)
		|| (x >= 97 && x <= 122))
	{
		return (1);
	}
	else
		return (0);
}
